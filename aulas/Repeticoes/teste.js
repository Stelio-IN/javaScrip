console.log('=======================While========================')
var n = 0
while(n<10){  
console.log(`${n}`)
n++
}
console.log('=======================DO_While========================')
var b = 10
do{
    console.log(`${b}`)
    b--
}while(b>=0)

console.log('=======================For========================')

for(var c=0;c<10;c++){
    console.log(`${c}`)
}